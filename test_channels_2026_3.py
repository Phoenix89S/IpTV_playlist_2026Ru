# ============================================
# ============================================================
#   ГОСТ Р 2026‑ZABAVA‑MEGA‑PROC‑M1
#   ОБРАБОТКА ИСТОЧНИКА MEGA_URL
#
#   ЛОГИРОВАНИЕ SKALA:
#   • SKALA_RAW   — фиксирует ВСЕ группы, пришедшие из MEGA (до канона)
#   • SKALA_OK    — группа прошла канон
#   • SKALA_DROP  — группа отсеяна каноном
#
#   НАЗНАЧЕНИЕ:
#   • Загрузить MEGA_URL
#   • Разобрать EXTINF → (title, url)
#   • Извлечь group-title из title
#   • Пропустить только группы, разрешённые каноном ZABAVA_CANON_GROUPS
#   • Вернуть очищенный список каналов
# ============================================================

def extract_group_from_title(title: str) -> str:
    """
    Извлекает group-title из строки EXTINF.
    Пример:
        "GitHub - Zabava / Москва | Первый канал" → "GitHub - Zabava / Москва"
    """
    if "|" in title:
        return title.split("|", 1)[0].strip()
    return title.strip()


def load_and_filter_mega() -> list[tuple[str, str]]:
    """
    Загружает MEGA_URL, логирует SKALA_RAW/OK/DROP,
    фильтрует группы по канону ZABAVA_CANON_GROUPS.
    Возвращает список (title, stream_url) только валидных каналов.
    """

    log_info("[MEGA] Загружаю и обрабатываю MEGA_URL…")
    raw_channels = load_playlist_from_url(MEGA_URL)

    filtered: list[tuple[str, str]] = []

    for title, stream_url in raw_channels:
        group = extract_group_from_title(title)

        # Проверка каноном + двойное логирование SKALA
        if zabava_group_allowed(group, logger=skala):
            filtered.append((title, stream_url))

    log_info(f"[MEGA] Готово. Пропущено каналов: {len(filtered)}")
    return filtered

