def load_raw(url: str) -> str:
    log_info(f"[LOAD] Загружаю: {url}")
    resp = requests.get(url, timeout=15)
    resp.raise_for_status()
    resp.encoding = resp.apparent_encoding or "utf-8"
    return resp.text


def load_playlist_from_url(url: str) -> list[tuple[str, str]]:
    """
    Загружаем M3U и возвращаем список (title, url).
    """
    text = load_raw(url)
    lines = text.splitlines()
    channels: list[tuple[str, str]] = []
    last_title = None

    for line in lines:
        line = line.strip()
        if not line:
            continue

        if line.startswith("#EXTINF"):
            # берём всё после запятой как название
            if "," in line:
                last_title = line.split(",", 1)[1].strip()
            else:
                last_title = None

        elif line.startswith("http"):
            if last_title:
                channels.append((last_title, line.strip()))
                last_title = None

    return channels

