def zabava_group_allowed(group_title: str, logger=skala) -> bool:
    """
    Проверка группы MEGA по канону ZABAVA_CANON_GROUPS.
    Логирование SKALA_RAW → SKALA_OK / SKALA_DROP.
    """

    # 1) ЛОГ ДО КАНОНА (фиксируем всё, что пришло)
    if logger:
        logger(f"RAW: {group_title}")

    # 2) ПРОВЕРКА КАНОНОМ
    if group_title in ZABAVA_CANON_GROUPS:
        if logger:
            logger(f"OK: {group_title}")
        return True

    # 3) ГРУППА НЕ В КАНОНЕ — ОТБРАСЫВАЕМ
    if logger:
        logger(f"DROP: {group_title}")

    return False

def normalize_region_code(code: str) -> str | None:
    digits = "".join(ch for ch in code if ch.isdigit())
    if not digits:
        return None
    if len(digits) > 2:
        digits = digits[-2:]
    if digits in REGION_MAP:
        return digits
    return None

